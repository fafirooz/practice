public class If {
    public static void main(String [] args) {
        int variable= 10;

        if(variable < 3){
            System.out.println("variable is less than 3");
        }
        else if (variable == 10) {
            System.out.println("10");
        }
        else {
            System.out.println("variable is bigger than 3");
        }

    }
}
